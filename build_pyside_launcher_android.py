"""
build_pyside_launcher_android.py — PySide 1 / Python 2.6 → Android APK builder

This is a FUNDAMENTALLY DIFFERENT approach from build_pyside_android.py.
Instead of compiling Shiboken/PySide/Qt from source (which has been bit-
rotting for 13 years), we use three PREBUILT artifacts published by the
PySide-for-Android project:

    1. PySideLauncher-debug.apk         — A signed APK that loads
                                          any PySide app from its
                                          assets/ folder at runtime.
    2. data_python.tgz                  — Python 2.6 runtime for ARM
                                          + compiled PySide 1 bindings.
    3. data_data_eu.licentia...qt.tgz   — Qt 4 libraries for ARM,
                                          targeting API-8 platform.

Build pipeline:

    Step 1  Preflight   — verify the user's project layout (main.py)
    Step 2  Workspace   — set up tmp dirs, download apktool
    Step 3  Download    — fetch the 3 artifacts (with mirror fallback)
    Step 4  Bundle      — zip the user's project as python_project.zip
    Step 5  Decode APK  — apktool d (extract resources, decode manifest)
    Step 6  Inject      — copy python_project.zip into assets/
                          (and the two tarballs if not already present)
    Step 7  Re-package  — optionally rename package + app label
    Step 8  Rebuild APK — apktool b
    Step 9  Zipalign    — required for Android runtime
    Step 10 Sign        — debug-keystore signing (apksigner / jarsigner)
    Step 11 Output      — copy final APK to user's --output-path

The whole thing runs on the GHA runner directly (no Docker needed) and
typically completes in 2-3 minutes.  Cold-cache it's about 5 min total
including downloads.

REQUIREMENTS on host:
  * Python 3.7+
  * java   (any JDK 8+, for apktool / jarsigner / keytool)
  * curl   (for downloads)
  * unzip  (usually preinstalled; for APK inspection)

Apktool, debug.keystore, and zipalign are downloaded / generated by this
script — no system install needed.
"""

