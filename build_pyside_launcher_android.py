# ------------------------------------------------------------------ helpers

def run(cmd, check=True, cwd=None, env=None, capture=False):
    """Print + run a subprocess command, returning the process result."""
    cmd_str = ' '.join(map(str, cmd))
    log.info('$ %s', cmd_str)
    try:
        result = subprocess.run(
            [str(c) for c in cmd],
            check=check,
            cwd=cwd,
            env=env,
            stdout=subprocess.PIPE if capture else None,
            stderr=subprocess.STDOUT if capture else None,
        )
        if capture:
            return result.stdout.decode('utf-8', 'replace')
        return result.returncode
    except subprocess.CalledProcessError as e:
        log.error('  ! command failed (exit %d): %s', e.returncode, cmd_str)
        raise


def download_with_fallback(urls, dest, min_bytes, label):
    """Try each URL in `urls` until one downloads successfully.

    Validates the downloaded file is at least `min_bytes` (rejects HTML
    error pages and short partial transfers).  Returns the destination
    path on success, raises SystemExit if all URLs fail.
    """
    dest = Path(dest)
    if dest.exists() and dest.stat().st_size >= min_bytes:
        log.info('  ✓ %s already cached: %s (%s)',
                 label, dest, _human_size(dest.stat().st_size))
        return dest

    dest.parent.mkdir(parents=True, exist_ok=True)
    for i, url in enumerate(urls, 1):
        log.info('  [%d/%d] %s', i, len(urls), url)
        if dest.exists():
            dest.unlink()
        try:
            run(['curl', '-L', '--fail', '--retry', '3', '--retry-delay', '5',
                 '--max-time', '600', '-A', 'Mozilla/5.0',
                 '-o', str(dest), url],
                check=False)
        except FileNotFoundError:
            raise SystemExit('  X curl is required but not installed.')
        if dest.exists() and dest.stat().st_size >= min_bytes:
            log.info('    ✓ downloaded %s (%s)',
                     dest.name, _human_size(dest.stat().st_size))
            return dest
        size = dest.stat().st_size if dest.exists() else 0
        log.warning('    ! got %d bytes (need >= %d), trying next mirror',
                    size, min_bytes)
    raise SystemExit(
        '  X all mirrors failed for {label}.\n'
        '    Tried:\n      {urls}\n'
        '    Workaround: download manually and place at {dest}'.format(
            label=label, urls='\n      '.join(urls), dest=dest))


def _human_size(n):
    """Format byte count as MB/KB."""
    if n >= 1024 * 1024:
        return '{:.1f} MB'.format(n / (1024.0 * 1024.0))
    if n >= 1024:
        return '{:.1f} KB'.format(n / 1024.0)
    return '%d bytes' % n


def java_available():
    """Check whether `java` is on PATH and runnable."""
    try:
        out = subprocess.run(['java', '-version'],
                             stdout=subprocess.PIPE,
                             stderr=subprocess.STDOUT)
        return out.returncode == 0
    except (FileNotFoundError, OSError):
        return False


def find_tool(name):
    """Locate a tool on PATH or return None."""
    from shutil import which
    return which(name)


